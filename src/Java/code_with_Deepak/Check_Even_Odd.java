package Java.code_with_Deepak;

public class Check_Even_Odd {
    public static void main(String[] args) {
        evenOddCheck(17);
    }
    public static void evenOddCheck(int a){
        if(a%2 == 0 ){
            System.out.println("Even No");
        }else{
            System.out.println("Odd No");
        }
    }
}
